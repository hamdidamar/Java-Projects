    private void btn_SecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SecActionPerformed

        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
       
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "jpg","png");
        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        if(result == JFileChooser.APPROVE_OPTION)
        {
            File selectedFile = file.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            lbl_resim.setIcon(ResimBoyutlandirma(path, null));
            resimYolu = path;
        }
        else{
            System.out.println("Lütfen seçim yapın");
        }
        
    }//GEN-LAST:event_btn_SecActionPerformed

    private void btn_EkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EkleActionPerformed
        // TODO add your handling code here:
        Baglanti bgl = new Baglanti();
        if (resimYolu !=null) {
            try {
            String ad = txt_Ad.getText();
            float fiyat = Float.parseFloat(txt_Fiyat.getText()) ;
            InputStream  img = new FileInputStream(new File(resimYolu));
            
            bgl.Ekle(ad,fiyat ,resimYolu);
            JOptionPane.showMessageDialog(null, "Ekleme işlemi başarılı..");
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Ana_Sayfa.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Ana_Sayfa.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        }else{
            JOptionPane.showMessageDialog(null, "Boş geçilmez");
        }
        Tabloda_Goster();
        
    }//GEN-LAST:event_btn_EkleActionPerformed

