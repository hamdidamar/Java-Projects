    public void Tabloda_Goster()
    {
        Baglanti bgl = new Baglanti();
        ArrayList<ilac> list = bgl.İlaclariGetir();
        DefaultTableModel model = (DefaultTableModel)tbl_Ilaclar.getModel();
        // clear jtable content
        model.setRowCount(0);
        Object[] row = new Object[4];
        for(int i = 0; i < list.size(); i++)
        {
            
            row[0] = list.get(i).getAdi();
            row[1] = list.get(i).getFiyat();
            
            
            model.addRow(row);
        }
    
    }
    
    public void IlacGoster(int index) throws SQLException
    {
        
        Baglanti bgl = new Baglanti();
        lbl_ID.setText(Integer.toString(bgl.İlaclariGetir().get(index).getId()));
        txt_Ad.setText(bgl.İlaclariGetir().get(index).getAdi());
        txt_Fiyat.setText(Float.toString(bgl.İlaclariGetir().get(index).getFiyat()));
        ImageIcon icon = ResimBoyutlandirma(bgl.İlaclariGetir().get(index).getResim(),null);
        resimYolu = bgl.İlaclariGetir().get(index).getResim();
        lbl_resim.setIcon(icon);
        
    }
